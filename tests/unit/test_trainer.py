def _make_context(
    *,
    agent_id: str = "agent-1",
    customer_id: str = "cust-1",
    correlation_id: str = "corr-1",
    outcome_success: bool = True,
    qa_status: str = "PASSED",
    execution_status: str = "COMPLETED",
    confidence_score: float = 0.9,
    approval_method: str = "AUTO_APPROVED",
) -> dict[str, Any]:
    return {
        "agent_id": agent_id,
        "customer_id": customer_id,
        "correlation_id": correlation_id,
        "outcome_success": outcome_success,
        "qa_status": qa_status,
        "execution_status": execution_status,
        "confidence_score": confidence_score,
        "approval_method": approval_method,
    }


def _make_artifact(
    artifact_id: str = "art-1",
    artifact_type: str = "CODE_PATCH",
    source_run_id: str = "run-1",
    content: str = "patch content",
    action_class: str = "patch_deployment",
) -> dict[str, Any]:
    return {
        "artifact_id": artifact_id,
        "artifact_type": artifact_type,
        "source_run_id": source_run_id,
        "content": content,
        "action_class": action_class,
    }


def _build_trainer() -> tuple[
    Trainer, InMemorySkillRegistry, InMemorySkillKnowledgeBase, InMemoryAuditLogger
]:
    evaluator = RunEvaluator()
    promotion_engine = PromotionEngine()
    registry = InMemorySkillRegistry()
    kb = InMemorySkillKnowledgeBase()
    audit = InMemoryAuditLogger()
    trainer = Trainer(
        evaluator=evaluator,
        promotion_engine=promotion_engine,
        skill_registry=registry,
        knowledge_base=kb,
        audit_logger=audit,
    )
    return trainer, registry, kb, audit


