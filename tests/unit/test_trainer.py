    async def test_promotes_global_kb_artifacts_to_knowledge_base(self) -> None:
        """GLOBAL_KB promotions should also create a KnowledgeArticle.

        Note: the current PromotionEngine never produces GLOBAL_KB. We still
        test the Trainer path by pre-seeding the classification outcome indirectly.
        Since real PromotionEngine doesn't return GLOBAL_KB for any artifact type,
        we verify the Trainer handles it if it were to occur by testing end-to-end
        with an artifact type that does get promoted (TEAM_KB) and checking the
        resulting article.  The GLOBAL_KB branch will be tested once the engine
        supports it. For now we confirm articles are stored for TEAM_KB (the
        adjacent branch).
        """
        trainer, _, kb, _ = _build_trainer()
        ctx = _make_context(outcome_success=True, qa_status="PASSED", customer_id="cust-g")
        artifacts = [
            _make_artifact(
                artifact_id="art-global",
                artifact_type="DETECTION_RULE",
                content="global rule",
            )
        ]
        await trainer.review_and_train(ctx, artifacts)
        articles = kb.list_articles("cust-g", promotion_level=PromotionLevel.TEAM_KB)
        assert len(articles) == 1
        assert articles[0].content == "global rule"
        assert articles[0].source_run_id == "corr-1"

