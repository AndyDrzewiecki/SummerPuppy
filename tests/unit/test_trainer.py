    async def test_multiple_artifacts_each_correct_treatment(self) -> None:
        trainer, _, kb, _ = _build_trainer()
        ctx = _make_context(
            outcome_success=True,
            qa_status="PASSED",
            customer_id="cust-multi",
        )
        artifacts = [
            _make_artifact(artifact_id="art-1", artifact_type="CODE_PATCH", content="patch1"),
            _make_artifact(artifact_id="art-2", artifact_type="RUNBOOK", content="runbook1"),
            _make_artifact(artifact_id="art-3", artifact_type="THREAT_REPORT", content="report1"),
        ]
        result = await trainer.review_and_train(ctx, artifacts)
        # CODE_PATCH -> TEAM_KB article, RUNBOOK -> PLAYBOOK, THREAT_REPORT -> RUN_RECORD (skip)
        articles = kb.list_articles("cust-multi", promotion_level=PromotionLevel.TEAM_KB)
        assert len(articles) == 1
        assert articles[0].content == "patch1"

        playbooks = kb.list_playbooks("cust-multi")
        assert len(playbooks) == 1
        assert playbooks[0].steps == ["runbook1"]

        # art-1 (TEAM_KB) and art-2 (PLAYBOOK_TEMPLATE) should be in kb_promotions
        assert "art-1" in result.kb_promotions
        assert "art-2" in result.kb_promotions
        # art-3 (RUN_RECORD) should NOT be in kb_promotions
        assert "art-3" not in result.kb_promotions

